/**
 * Calculates the result of two numbers.
 * @param {number} var1 - The first number to be calculated.
 * @param {number} var2 - The second number to be calculated.
 * @returns {number} The result of the calculation.
 */
function calculateNumbers(var1, var2) {
    let total = var1 + var2;
    return total;
}
